"""What is tuple? Difference between list and tuple"""

#Tuple is a collection which is ordered and unchangeable. Allows duplicate members
tuple1 = (1, 2, 3)
# my_tuple[0] = 4 # tuples is immutable
print(f'This is a Tuple: {tuple1}')



list1 = [1, 2, 3]
list1[0] = 4  # change the value is mutable
print(f'This is a List: {list1}')